"""
Operadores

Son utilizados para verificar condiciones y juzgar si éstas son verdaderas o falsas.
"""

#Operadores de Igualdad
"""
(=) Igualdad: ==
(≠) Diferencia: !=
(x == y) x es igual a y
(x != y) x es diferente de y
"""

a = False
b = True
print(a==b)

#Operadores diferenciales
"""
(>) Mayor que: >
(<) Menor que: <
(≥) Mayor o igual que: >=
(≤) Menor o igual que: <=
"""
c = 3
d = 5
print(c > d)
print(c < d)
print(c >= d)
print(c <= d)
e = 'a'
f = 'b'
print(e > f)
print(e < f)
print(e >= f)
print(e <= f)
