"""
Operaciones matemáticas

Son organizadas a través de los operadores tradicionales, que también se encuentran incluidos en el lenguaje
"""

#Suma
print(10+10)
print(10+(50+50))

#Resta
print(10-10)
print(1000-80)

#División
#Por defecto, en python se imprimen decimales en las diviciones
print(10/5)
#Se imprime varias decimales
print(10/6)
#Redondear hacia abajo el resultado con "//" y el tipo de dato lo cambia a entero (int)
print(10//6)

#Multiplicación
print(10*800)
print(55*5)